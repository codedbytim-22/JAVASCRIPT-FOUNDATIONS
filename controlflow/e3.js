/*
Create a variable gradeScore.

If score ≥ 90 → "Grade A"

Else if score ≥ 80 → "Grade B"

Else if score ≥ 70 → "Grade C"

Else → "Fail"

Print the result.
*/

let gradescore = 90;
if (gradescore >= 90) {
  console.log('Grade A');
} else if (gradescore >= 80) {
  console.log('Grade B');
} else if (gradescore >= 70) {
  console.log('Grade C');
} else {
  console.log('Fail');
}
